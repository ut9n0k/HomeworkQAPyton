def amount(a: int, b: int):
    return a + b


def multiply(a: int, b: int):
    return a * b


def divide(a: int, b: int):
    return a / b


def subtract(a: int, b: int):
    return a - b


if __name__ == "__main__":
    print(amount(20, 4))
    print(multiply(20, 4))
    print(divide(20, 4))
    print(subtract(20, 4))
